package chap04;

public class Changevalue2 {
    public static void main(String[] args) {

        int num1 = 7;
        int num2 = 4;

        System.out.print("num1 = ");
        System.out.println(num1);
        System.out.print("num2 = ");
        System.out.println(num2);

        System.out.println("************");

        num2 = num1;

        System.out.print("num1 = ");
        System.out.println(num1);
        System.out.print("num2 = ");
        System.out.println(num2);

    }
}

package chap05;

public class PlusOperator {

    public static void main(String[] args) {

        int i = 5;
        int j = 2;

        System.out.println(j + "행 문자 " + i + "행의 프로그램으로 표시함!");
        System.out.println("실제로 " + j + "행의 프로그램으로 표시하는 것도 가능!");

        System.out.println("i + j = " + i + j);
        System.out.println("i + j = " + (i + j));

    }
}

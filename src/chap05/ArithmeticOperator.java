package chap05;

public class ArithmeticOperator {

    public static void main(String[] args) {
        int a = 9;
        int b = 4;

        int answer;

        System.out.print("a = ");
        System.out.println(a);
        System.out.print("b = ");
        System.out.println(b);

        answer = a + b;
        System.out.print("a + b = ");
        System.out.println(answer);

        answer = a - b;
        System.out.print("a - b = ");
        System.out.println(answer);

        answer = a * b;
        System.out.print("a * b = ");
        System.out.println(answer);

        answer = a / b;
        System.out.print("a / b = ");
        System.out.println(answer);

        answer = a % b;
        System.out.print("a % b = ");
        System.out.println(answer);

    }
}
